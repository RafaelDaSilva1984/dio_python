#Operadores Atribuição

#atribuição Simples
saldo = 500
print(f'Atribuindo simples de 500 ao saldo = {saldo}')
#atribuição com Adição

saldo += 200
print(f'Atruibuição com adição = {saldo}')
#atribuição com subtração

saldo -= 200
print(f'Atruibuição com subtração = {saldo}')
#atribuição com divisão casas

saldo /= 2
print(f'Atruibuição com divisão casas = {saldo}')
#atribuição com divisão inteira

saldo //= 2
print(f'Atruibuição com divisão inteira = {saldo}')
#atribuição com exponeciação

saldo **= 2
print(f'Atruibuição com exponenciação = {saldo}')