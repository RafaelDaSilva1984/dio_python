nome = "Guilherme Arthur de Carvalho"
print(nome[0])#Caracter 0
print(nome[-1])#Caracter -1 último caracter
print(nome[:9])#até caracter 9
print(nome[10:])#apartir caracter 10
print(nome[10:16])#apartir caracter 10 start até 16 stop
print(nome[10:16:2])#apartir caracter 10 até 16 de 2 em dois caracteres
print(nome[:])#cópia da string sem start e stop
print(nome[::-1])#Cria cópia invertida sem start e stop


